# Creating a set
my_set = {1, 2, 3, 4}
print(my_set)  # Output: {1, 2, 3, 4}

# Creating a set using the set() constructor
my_set2 = set([3, 4, 5])
print(my_set2)  # Output: {3, 4, 5}

# Demonstrating that sets automatically remove duplicates
dup_set = {1, 2, 2, 3, 3}
print(dup_set)  # Output: {1, 2, 3}