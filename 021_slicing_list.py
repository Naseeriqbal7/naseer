numbers = [5, 10, 15, 20, 25, 30, 35, 40, 45, 50, 55, 60]

# Print the first two elements of the list using slicing
print(numbers[0:2])

# Print the first two elements of the list using shorthand slicing
print(numbers[:2])

# Print all elements from the 5th element to the end of the list
print(numbers[4:])

# Print every second element from the start to the 4th element (exclusive)
print(numbers[0:4:2])

# Print the list in reverse order
print(numbers[::-1])

# Print every second element of the list starting from the first element
print(numbers[::2])

# Print every second element of the list starting from the second element
print(numbers[1::2])