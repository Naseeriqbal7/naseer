from datetime import datetime

some_day = datetime(2025, 10, 28, 14, 40, 30)

print("Year:", some_day.year)
print("Month:", some_day.month)
print("Day:", some_day.day)
print("Hour:", some_day.hour)
print("Minute:", some_day.minute)
print("Second:", some_day.second)
print("Microsecond:", some_day.microsecond)